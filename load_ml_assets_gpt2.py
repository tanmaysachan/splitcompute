        #     to_write = list(itertools.chain(*to_write))
        #     f.write(struct.pack('%sf' % len(to_write), *to_write))

        for block in self.transformer.h[:3]:
            x = block(x)

        x = self.transformer.ln_f(x)
        logits = self.lm_head(x)
        return logits

